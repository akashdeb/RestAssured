package pojoLibraryForJira;

public class PojoClassForContent2 {
	public String type;
	public String text;
	
	
	public PojoClassForContent2(String type, String content) {
		super();
		this.type = type;
		this.text = text;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return text;
	}
	public void setContent(String text) {
		this.text = text;
	}
	
	
	
}
